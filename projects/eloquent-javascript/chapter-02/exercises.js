// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(number) {
  let string = ""
  for (var i = 0; i < number; i++){
    for (var j = 0; j < number; j++){
      // string += " "
      if ((i + j) % 2 === 0){
        string += " "
      } else {
        string += "#"
      }
    }
    string += "\n"
  }
  console.log(string)
}
console.log(drawChessboard());

