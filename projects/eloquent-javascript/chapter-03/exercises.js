// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x,y) {
  if (x < y){
    return x
  } else {
    return y
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(number) {
  if (number === 1){
    return false
  } else if (number === 0){
    return true
  }
  if (number < 0){
    return isEven(-number)
  }
  if (number > 1){
    return isEven(number - 2)
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(word, char) {
  let count = 0;
  for (var i = 0; i < word.length; i++){
    if (word[i] === char){
      count++
    }
  }
  return count 
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(word) {
  var count = 0;
  for (var i = 0; i < word.length; i++){
    if (word[i] === "B") {
      count++
    }
  }
  return count 
}

